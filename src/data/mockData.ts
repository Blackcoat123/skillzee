// Get skill with trainer info
export const getSkillWithTrainer = (skillId: string) => {
  const skill = mockSkills.find(s => s.id === skillId);
  if (!skill) return null;
  const trainer = mockUsers.find(u => u.id === skill.trainer_id);
  return { ...skill, trainer };
};

// Get booking with details
export const getBookingWithDetails = (bookingId: string) => {
  const booking = mockBookings.find(b => b.id === bookingId);
  if (!booking) return null;
  const skill = mockSkills.find(s => s.id === booking.skill_id);
  const trainer = mockUsers.find(u => u.id === booking.trainer_id);
  return { ...booking, skill, trainer };
};
